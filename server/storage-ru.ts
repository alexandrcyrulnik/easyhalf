export class MemStorageRU implements IStorage {
  async getPlan(planType: PlanType): Promise<TrainingPlan | undefined> {
    let weeks: WeekData[];
    let name: string;
    let goalTime: string;
    let totalWeeks: number;

    switch (planType) {
      case "1:30":
        weeks = get130PlanRU();
        name = "План Sub 1:30";
        goalTime = "1:30";
        totalWeeks = 15;
        break;
      case "1:45":
        weeks = get145PlanRU();
        name = "План Sub 1:45";
        goalTime = "1:45";
        totalWeeks = 13;
        break;
      case "2:00":
        weeks = get200PlanRU();
        name = "План Sub 2:00";
        goalTime = "2:00";
        totalWeeks = 15;
        break;
      default:
        return undefined;
    }

    return { id: planType, name, goalTime, totalWeeks, weeks };
  }

  async getGuide(): Promise<IntroductionContent> {
    return { sections: generateGuideRU() };
  }

  async getStrengthConditioning(): Promise<IntroductionContent> {
    return { sections: generateStrengthConditioningRU() };
  }
}

